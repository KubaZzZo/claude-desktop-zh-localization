def update_locale() -> int:
    data = json.loads(ION_LOCALE.read_text(encoding="utf-8"))
    changed = 0
    for key, value in SETTINGS_TRANSLATIONS.items():
        if data.get(key) != value:
            data[key] = value
            changed += 1
    ION_LOCALE.write_text(json.dumps(data, ensure_ascii=False, indent=2, sort_keys=True) + "\n", encoding="utf-8")
    return changed


def update_patches() -> int:
    data = json.loads(PATCHES.read_text(encoding="utf-8"))
    by_description = {item.get("description"): item for item in data}
    changed = 0
    for patch in PATCH_TRANSLATIONS:
        existing = by_description.get(patch["description"])
        if existing is None:
            data.append(patch)
            changed += 1
        elif any(existing.get(key) != value for key, value in patch.items()):
            existing.update(patch)
            changed += 1
    PATCHES.write_text(json.dumps(data, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    return changed


def main() -> int:
    locale_changes = update_locale()
    patch_changes = update_patches()
    print(f"locale changes: {locale_changes}")
    print(f"patch changes: {patch_changes}")
    return 0


